package com.unknown.model.response;

public class BuildingResponse {
	private String name;
	private String ward;
	private String street;
	private Integer numberOfBasement;
	private String discribe;
	private Integer status;
	private String managerName;
	private String managerPhoneNumber;
	private Integer districtName;
	private Integer buildingTypeName;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getWard() {
		return ward;
	}

	public void setWard(String ward) {
		this.ward = ward;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public Integer getNumberOfBasement() {
		return numberOfBasement;
	}

	public void setNumberOfBasement(Integer numberOfBasement) {
		this.numberOfBasement = numberOfBasement;
	}

	public String getDiscribe() {
		return discribe;
	}

	public void setDiscribe(String discribe) {
		this.discribe = discribe;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public String getManagerName() {
		return managerName;
	}

	public void setManagerName(String managerName) {
		this.managerName = managerName;
	}

	public String getManagerPhoneNumber() {
		return managerPhoneNumber;
	}

	public void setManagerPhoneNumber(String managerPhoneNumber) {
		this.managerPhoneNumber = managerPhoneNumber;
	}

	public Integer getDistrictName() {
		return districtName;
	}

	public void setDistrictName(Integer districtName) {
		this.districtName = districtName;
	}

	public Integer getBuildingTypeName() {
		return buildingTypeName;
	}

	public void setBuildingTypeName(Integer buildingTypeName) {
		this.buildingTypeName = buildingTypeName;
	}

}
